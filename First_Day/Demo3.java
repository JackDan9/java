//comparision operator
class Demo3
{
	public static void main(String[] args)
	{
		System.out.println(4 == 5);//false
		System.out.println(4 != 5);//true
		
		System.out.println(5 > 5);//false
		System.out.println(5 < 5);//false
		
		System.out.println(5 >= 5);//true
		System.out.println(5 <= 5);//true
	}
}